// 当前语言（默认中文）
let currentLang = localStorage.getItem('language') || 'zh-CN';

// 切换语言
function switchLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);

    // 更新 HTML lang 属性
    document.documentElement.lang = lang;

    // 更新所有带 data-i18n 属性的元素
    updatePageText();

    // 更新语言切换按钮文字
    updateLangButton();
}

// 更新页面文本
function updatePageText() {
    const elements = document.querySelectorAll('[data-i18n]');

    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = translations[currentLang][key];

        if (translation) {
            // 如果是 input 或 textarea，更新 placeholder
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        }
    });
}

// 更新语言切换按钮
function updateLangButton() {
    const langText = document.querySelector('.lang-text');
    if (langText) {
        langText.textContent = currentLang === 'zh-CN' ? 'EN' : '中文';
    }
}

// 初始化国际化
function initI18n() {
    // 应用当前语言
    updatePageText();
    updateLangButton();

    // 绑定语言切换按钮
    const langToggle = document.getElementById('langToggle');
    if (langToggle) {
        langToggle.addEventListener('click', () => {
            const newLang = currentLang === 'zh-CN' ? 'en' : 'zh-CN';
            switchLanguage(newLang);
        });
    }

    console.log(`🌐 语言已设置为: ${currentLang === 'zh-CN' ? '中文' : 'English'}`);
}

// 页面加载时初始化
document.addEventListener('DOMContentLoaded', initI18n);

// 导出函数供外部使用
window.i18n = {
    switchLanguage,
    getCurrentLang: () => currentLang,
    translate: (key) => translations[currentLang][key] || key
};
